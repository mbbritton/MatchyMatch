/**
 * Pick a word for today (deterministic per calendar day).
 */
export function pickDailyWord() {
  const now   = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const day   = Math.floor((now - start) / 86_400_000);
  return SCRAMBLE_WORDS[day % SCRAMBLE_WORDS.length];
}

/**
 * Scramble a word string, guaranteeing the result differs from the original.
 */
export function scrambleWord(word) {
  const letters = word.split("");
  let scrambled;
  let attempts = 0;
  do {
    for (let i = letters.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [letters[i], letters[j]] = [letters[j], letters[i]];
    }
    scrambled = letters.join("");
    attempts++;
  } while (scrambled === word && attempts < 20);
  return scrambled;
}
